"""
Write a program to find the index of an element in a tuple.
"""

# Define a tuple
my_tuple = (1, 2, 3, 4, 5)

# Element to find
element_to_find = 3

# Find the index of the element
index = my_tuple.index(element_to_find)

# Print the result
print(f"The index of {element_to_find} in the tuple is {index}.")